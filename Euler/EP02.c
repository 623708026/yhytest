#include<stdio.h>

int main() {
    int a = 1, b = 2, ans = 0;
    while (b < 4000000) {
        if (b % 2 == 0) ans += b;
        b = a + b;
        a = b - a;
    }
    printf("%d\n", ans);
    return 0;
}
